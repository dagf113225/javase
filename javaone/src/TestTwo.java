
public class TestTwo {
	
	public static void main(String[] args) {
		
		//java的基本数据类型
		
		//1.字节类型
		byte  a1=100;
		
		System.out.println(a1);
		
		
		//2.短整型
		short  a2=200;
		
		System.out.println(a2);
		
		
		//3.整型
		int  a3=300;
		System.out.println(a3);
		
		
		//4.长整型
		long  a4=6000;
		System.out.println(a4);
		
		System.out.println("----------------------");
		
		
		//5.boolean类型
		boolean  flag1 =true;
		
		System.out.println(flag1);
		
		boolean  flag2 =false;
		System.out.println(flag2);
		
		System.out.println("----------------------");
		
		//6.单精度浮点类型,
		float   f1=20.0f;
		System.out.println(f1);
		
		//7.双进度 浮点类型。默认是double 类型
		double d1 =20.0;
		System.out.println(d1);
		
		System.out.println("----------------------");
		
		
		//8.字符类型
		char  cf='a';
		System.out.println(cf);
		
		
		
		
		
		
		
		
	}

}
