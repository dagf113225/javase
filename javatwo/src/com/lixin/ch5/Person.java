package com.lixin.ch5;

public class Person  
{
	
	//姓名
	String name;
	
	//年龄
	int  age;
	
	//性别
	char  sex;

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public int getAge()
	{
		return age;
	}

	public void setAge(int age)
	{
		this.age = age;
	}

	public char getSex()
	{
		return sex;
	}

	public void setSex(char sex)
	{
		this.sex = sex;
	}
	
	

}
